// Helper functions
export const getUniqueRestaurantValues = (key) => {
  if (key === 'city') {
    return [...new Set(mockRestaurants.map(r => r.city))];
  }
  if (key === 'status') {
    return ['active', 'inactive'];
  }
  if (key === 'hotel_name') {
    return [...new Set(mockRestaurants.map(r => r.hotel_name))];
  }
  return [];
};

export const filterRestaurants = (restaurants, filters) => {
  return restaurants.filter(restaurant => {
    const matchesSearch = !filters.search || 
      restaurant.name.toLowerCase().includes(filters.search.toLowerCase()) ||
      restaurant.hotel_name.toLowerCase().includes(filters.search.toLowerCase()) ||
      restaurant.cuisine_types.some(c => c.toLowerCase().includes(filters.search.toLowerCase()));
    
    const matchesCity = !filters.city || restaurant.city === filters.city;
    const matchesStatus = !filters.status || restaurant.status === filters.status;

    return matchesSearch && matchesCity && matchesStatus;
  });
};

export const getRestaurantStats = (restaurants) => {
  return {
    total: restaurants.length,
    active: restaurants.filter(r => r.status === 'active').length,
    inactive: restaurants.filter(r => r.status === 'inactive').length,
    avgRating: (restaurants.reduce((sum, r) => sum + r.rating, 0) / restaurants.length).toFixed(1),
    totalSeating: restaurants.reduce((sum, r) => sum + r.seating_capacity, 0)
  };
};
